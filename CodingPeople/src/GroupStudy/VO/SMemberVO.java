package GroupStudy.VO;

public class SMemberVO {
	private String memId;
	private String smemberStatusId;
	private String smemberDate;
    private String sgroupId;
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getSmemberStatusId() {
		return smemberStatusId;
	}
	public void setSmemberStatusId(String smemberStatusId) {
		this.smemberStatusId = smemberStatusId;
	}
	public String getSmemberDate() {
		return smemberDate;
	}
	public void setSmemberDate(String smemberDate) {
		this.smemberDate = smemberDate;
	}
	public String getSgroupId() {
		return sgroupId;
	}
	public void setSgroupId(String sgroupId) {
		this.sgroupId = sgroupId;
	}
    
}
